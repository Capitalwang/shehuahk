    <td colspan="2" rowspan="2" width="453" height="605" valign="top" class="LD_ny"><strong>「钻石起源」</strong><br>
	&nbsp;&nbsp;&nbsp;&nbsp;钻石是世界上最珍贵的宝石，其居世界五大珍贵高档宝石之首，素有“宝石之王”、“无价之宝”的美誉。它是大自然的杰作。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;天然钻石原产于超过200公里深的地下，它们的形成年代通常为24亿到32亿年以前，但是也有短至6千万年的。钻石是地壳中碳元素在经历了剧烈的热量和压力作用下形成的，然后被埋在地壳几乎最深的地方。这种地壳深处的钻石有时会随着地壳深处熔岩的上升而被带到地壳表面。携带钻石的熔岩凝固时会形成上大下小的喇叭状岩柱，岩柱中往往含有钻石。<br>
　　&nbsp;&nbsp;&nbsp;&nbsp;根据比利时安特卫普钻石高级委员会所述，要将250吨的岩石、沙和砾石处理之后才可能得到一克拉的钻石胚，一克拉钻石胚经切磨，只能保留40%~50%的重量。一克拉等于0.2克，250吨等于2亿5千万克。另外，开采出来的钻石大部分品质不佳，只能用作工业使用的磨料或钻头。能用作宝石的钻石往往不到钻石原矿的七分之一。<br><br>
	<strong>「钻石产地」</strong><br>
	&nbsp;&nbsp;&nbsp;&nbsp;世界各地均有钻石产出，已有三十多个国家拥有钻石资源，年产量一亿克拉左右。产量前五位的国家是澳大利亚、扎伊尔、博茨瓦纳、俄罗斯、南非。这五个国家的钻石产量占全世界钻石产量的90%左右。其它产钻石的国家有刚果(金)、 巴西、圭亚那、委内瑞拉、安哥拉、中非、加纳、几内亚、象牙海岸、利比利亚、纳米比亚、塞拉利昂、坦桑尼亚、津巴布韦、印度尼西亚、印度、中国、加拿大等。<br>
    &nbsp;&nbsp;&nbsp;&nbsp;对于钻石的毛坯和宝石级钻石所占比例来说，最好的钻石来自于纳米比亚冲积矿床中开采出来的钻石。对切磨好的钻石戒面，很难分辩出产自哪个国家和矿区。任何矿区产出的钻石都有好、中、差。拿矿区中最好的钻石与纳米比亚产出最差的钻石比，纳米比亚冲积矿床的钻石也不一定就好。
